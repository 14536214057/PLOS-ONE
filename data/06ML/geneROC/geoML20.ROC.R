#???УА?
library(glmnet)
library(pROC)

expFile="merge.normalize.txt"      #?????????ФМ?
geneFile="modelGene.list.txt"      #?????аБ??ФМ?
setwd("C:\\Users\\1453621405\\Desktop\\IPF\\100\\ML\\geneROC")    #???УЙ???ФПТМ

#??ШЁ?????????ФМ?
rt=read.table(expFile, header=T, sep="\t", check.names=F, row.names=1)

#??ШЁ??ЦЗ?ФЗ?????ЯЂ(????????ЪЕ????)
y=gsub("(.*)\\_(.*)\\_(.*)", "\\3", colnames(rt))
y=ifelse(y=="Control", 0, 1)

#??ШЁ?????аБ??ФМ?
geneRT=read.table(geneFile, header=F, sep="\t", check.names=F)

#????ЭМ?ЮЕ???ЩЋ
bioCol=rainbow(nrow(geneRT), s=0.9, v=0.9)

#?дЛ???????б­????????ROC????
aucText=c()
k=0
for(x in as.vector(geneRT[,1])){
	k=k+1
	#????ROC????
	roc1=roc(y, as.numeric(rt[x,]))     #?УЕ?ROC???пЕФВ???
	if(k==1){
		pdf(file="ROC.genes.pdf", width=6, height=6)
		plot(roc1, print.auc=F, col=bioCol[k], legacy.axes=T, main="", lwd=3)
		aucText=c(aucText, paste0(x,", AUC=",sprintf("%.3f",roc1$auc[1])))
	}else{
		plot(roc1, print.auc=F, col=bioCol[k], legacy.axes=T, main="", lwd=3, add=TRUE)
		aucText=c(aucText, paste0(x,", AUC=",sprintf("%.3f",roc1$auc[1])))
	}
}
#????ЭМ?????УЕ?ROC?????ТЕ?????
legend("bottomright", aucText, lwd=3, bty="n", cex=0.8, inset = c(0.05, 0), col=bioCol[1:(ncol(rt)-1)])
dev.off()


